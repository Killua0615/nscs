/** Get note for a question by matching its ID or prefix */
export function getNoteForQuestion(questionId: string): string | undefined {
  // Try exact question ID first (for per-exercise notes)
  if (QUESTION_NOTES[questionId]) return QUESTION_NOTES[questionId];
  // Fall back to subcategory prefix: q-1a-a-01 → q-1a-a
  const prefix = questionId.replace(/-\d+$/, '');
  return CATEGORY_NOTES[prefix];
}
